// Validate airport code format
export const isValidAirportCode = (code) => {
  if (!code) return false;
  const cleanCode = code.trim().toUpperCase();
  // Allow 3-4 character codes, letters and numbers
  return /^[A-Z0-9]{3,4}$/.test(cleanCode);
};